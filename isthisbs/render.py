# --------------------------------------------------------------------------- #
# Public contract
# --------------------------------------------------------------------------- #


def render_site(checks: list[Check], out_dir: Path) -> None:
    """Render the entire site from ``checks`` into ``out_dir``.

    ``checks`` is the newest-first list produced by ``content.build_checks``.
    All groupings (sections, entity hubs, collections, pagination) are derived
    here so the caller only has to pass the flat list. Idempotent: writes files,
    never reads prior output.
    """
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    css_path, css_body = _css_asset()
    env = _build_env()
    env.globals["CSS_PATH"] = css_path

    # Resolve related refs against *locally rendered* checks only — never link
    # to a claim that isn't on this site.
    by_id: dict[str, Check] = {c.verification_id: c for c in checks}

    sections = group_by_section(checks)  # every section key present (may be [])
    entity_groups = group_by_entity(checks)  # only entities with >= 2 claims
    colls = collections(checks)  # {"bs_files": [...], "checks_out": [...]}

    _render_home(env, out_dir, checks, sections, colls, entity_groups)
    _render_sections(env, out_dir, sections)
    topic_slugs = {g.entity.slug for g in entity_groups}
    _render_articles(env, out_dir, checks, by_id, topic_slugs)
    _render_topics(env, out_dir, entity_groups)
    _render_latest(env, out_dir, checks)
    _render_collections(env, out_dir, colls)
    _render_search(env, out_dir)
    _render_about(env, out_dir)
    _render_claim_stubs(env, out_dir, checks)
    _render_404(env, out_dir)

    _copy_static(out_dir, css_path, css_body)

    logger.info(
        "Rendered %d articles, %d section hubs, %d topics into %s",
        len(checks),
        len(SECTIONS),
        len(entity_groups),
        out_dir,
    )


