def _build_env() -> Environment:
    """Configure the shared Jinja2 environment (globals + filters)."""
    env = Environment(
        loader=FileSystemLoader(str(_TEMPLATE_DIR)),
        autoescape=select_autoescape(["html", "xml"]),
        trim_blocks=True,
        lstrip_blocks=True,
    )
    # Globals available in every template.
    env.globals["SITE"] = SITE
    env.globals["SECTIONS"] = SECTIONS
    env.globals["VERDICT_ORDER"] = VERDICT_ORDER
    env.globals["SOURCES_SHOWN_MAX"] = SOURCES_SHOWN_MAX
    env.globals["build_date"] = datetime.now(UTC).date()

    def claim_size_class(claim: str) -> str:
        """Graduated hero sizing — fold economics beat spectacle.

        Short claims earn the full display size; anything longer steps down
        so the verdict meter and the first answer paragraph stay above the
        fold on ordinary laptop viewports (measured: a 136-char claim at
        56px pushed the meter to 710px and the answer to 908px).
        """
        n = len(claim or "")
        if n > 120:
            return " claim-quote--long"
        if n > 60:
            return " claim-quote--medium"
        return ""

    env.globals["claim_size_class"] = claim_size_class
    # GA4 is opt-in at build time: set GA_MEASUREMENT_ID (e.g. a GitHub Actions
    # variable) and the gtag snippet ships; unset → zero analytics markup.
    # Never hardcoded — the public repo stays configuration-free.
    env.globals["GA_MEASUREMENT_ID"] = os.environ.get("GA_MEASUREMENT_ID", "")

    # Custom filters.
    env.filters["datefmt"] = _datefmt
    env.filters["jsonld"] = _jsonld
    return env


def _datefmt(value: str | datetime | date | None) -> str:
    """Format an ISO string or datetime as ``"Jul 23, 2026"``.

    Tolerant: empty/unparseable input returns the original string (or "").
    Uses explicit day/year interpolation instead of ``%-d`` so the output is
    identical across platforms.
    """
    if not value:
        return ""
    if isinstance(value, datetime):
        dt: datetime | date = value
    elif isinstance(value, date):
        dt = value
    else:
        try:
            dt = datetime.fromisoformat(str(value).replace("Z", "+00:00"))
        except ValueError:
            return str(value)
    return f"{dt:%b} {dt.day}, {dt.year}"


def _jsonld(data: dict[str, Any]) -> Markup:
    """Serialize a JSON-LD dict into a safe ``<script>`` block.

    ``ensure_ascii=False`` keeps accented source names/claims human-readable;
    ``<`` is escaped to its unicode form so a stray ``</script>`` in the data
    can never break out of the tag (the payload stays valid JSON either way).
    """
    payload = json.dumps(data, ensure_ascii=False, separators=(",", ":"))
    payload = payload.replace("<", "\\u003c")
    return Markup(f'<script type="application/ld+json">{payload}</script>')


