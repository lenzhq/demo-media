"""HTML rendering: Check objects → the full static site under ``out_dir``.

This is the design layer. It owns exactly one public function,
:func:`render_site`, which ``build.py`` calls after the data layer has parsed
the cache into ``Check`` objects. Everything else here is private plumbing:

* a configured Jinja2 ``Environment`` (templates in ``templates/``),
* two custom filters (``datefmt``, ``jsonld``),
* per-page-type render helpers that compute groupings via ``content`` and
  hand ready-made JSON-LD dicts (built in ``seo``) to the templates.

Every page is written as ``<path>/index.html`` so the site works as plain
directories on any static host (clean URLs, no server rewrites). The one
exception is ``404.html`` at the site root, which Firebase serves for misses.

No network, no secrets, no state — a pure function of its ``checks`` argument.
"""

