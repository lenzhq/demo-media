def _truncate(text: str, limit: int) -> str:
    """Trim to ``limit`` chars on a word boundary, adding an ellipsis."""
    text = " ".join((text or "").split())
    if len(text) <= limit:
        return text
    return text[:limit].rsplit(" ", 1)[0].rstrip(",.;:—- ") + "…"


def _meta_description(check: Check) -> str:
    """Verdict-first meta description with a receipts hook (SEO round 2).

    ``Verdict: X.`` catches is-it-true/false queries; the source count is the
    trust signal competitor snippets don't have. Budgeted to ~158 chars.
    """
    paras = check.summary_paragraphs
    body = paras[0] if paras else SITE.description
    # First sentence of the summary (fall back to the trimmed paragraph).
    sentence = body.split(". ")[0].rstrip(".") + "."
    prefix = f"Verdict: {check.verdict.key}. "
    suffix = (
        f" Checked against {len(check.sources)} independent sources."
        if check.sources
        else ""
    )
    budget = 158 - len(prefix) - len(suffix)
    return f"{prefix}{_truncate(sentence, max(40, budget))}{suffix}"


def _feed_path(base_path: str, page_no: int) -> str:
    """Site path for page ``page_no`` (1-based) of a paginated feed.

    Page 1 lives at ``base_path`` itself; later pages nest under ``page/N/``.
    """
    if page_no <= 1:
        return base_path
    return f"{base_path}page/{page_no}/"


def _write(out_dir: Path, url_path: str, html: str) -> None:
    """Write ``html`` to the clean-URL location for ``url_path``.

    ``"/"`` → ``out_dir/index.html``;
    ``"/health/"`` → ``out_dir/health/index.html``.
    """
    rel = url_path.strip("/")
    target = (out_dir / rel / "index.html") if rel else (out_dir / "index.html")
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(html, encoding="utf-8")


def _resolve_related(
    check: Check, by_id: dict[str, Check], all_checks: list[Check]
) -> list[Check]:
    """Resolve a check's related refs to local Check objects (drop the rest).

    When the API gave us nothing (the related endpoint needs an API key;
    keyless builds get ``related: []``), fall back to local entity overlap so
    "MORE CHECKS" still renders.
    """
    resolved: list[Check] = []
    seen: set[str] = {check.verification_id}
    for ref in check.related:
        if ref.verification_id in seen:
            continue
        local = by_id.get(ref.verification_id)
        if local is not None:
            resolved.append(local)
            seen.add(ref.verification_id)
        if len(resolved) >= RELATED_LIMIT:
            break
    if not resolved:
        resolved = related_fallback(check, all_checks, limit=RELATED_LIMIT)
    return resolved


def _entity_cloud(groups: list[EntityGroup]) -> list[dict[str, Any]]:
    """Top entities for the home cloud, each with a 0..1 weight for sizing."""
    top = groups[:_HOME_CLOUD_SIZE]
    if not top:
        return []
    counts = [len(g.checks) for g in top]
    lo, hi = min(counts), max(counts)
    span = (hi - lo) or 1
    return [
        {
            "entity": g.entity,
            "count": len(g.checks),
            "weight": round((len(g.checks) - lo) / span, 3),
        }
        for g in top
    ]


