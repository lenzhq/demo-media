def _render_home(
    env: Environment,
    out_dir: Path,
    checks: list[Check],
    sections: dict[str, list[Check]],
    colls: dict[str, list[Check]],
    entity_groups: list[EntityGroup],
) -> None:
    """The front page: lead + fresh rail + strips + section blocks + cloud."""
    lead = pick_lead(checks)  # newest well-receipted check (E1 editorial floor)
    lead_id = lead.verification_id if lead else ""

    def sans_lead(items: list[Check]) -> list[Check]:
        """The lead story never repeats in another front-page slot."""
        return [c for c in items if c.verification_id != lead_id]

    rail = sans_lead(checks)[:HOME_RAIL_SIZE]

    # Per-section blocks: only sections that actually have checks, in nav order.
    section_blocks = [
        {"section": SECTIONS[key], "checks": sans_lead(items)[:HOME_SECTION_SIZE]}
        for key, items in sections.items()
        if sans_lead(items)
    ]

    html = env.get_template("home.html").render(
        nav_active="home",
        canonical_path="/",
        page_title=f"{SITE.name} — {SITE.tagline}",
        meta_description=SITE.description,
        og_type="website",
        og_image_path=_DEFAULT_OG_PATH,
        jsonld_blocks=[seo.organization(), seo.website()],
        lead=lead,
        rail=rail,
        bs_files=sans_lead(colls["bs_files"])[:HOME_STRIP_SIZE],
        checks_out=sans_lead(colls["checks_out"])[:HOME_STRIP_SIZE],
        section_blocks=section_blocks,
        entity_cloud=_entity_cloud(entity_groups),
    )
    _write(out_dir, "/", html)


def _render_sections(
    env: Environment, out_dir: Path, sections: dict[str, list[Check]]
) -> None:
    """8 section hubs, each paginated at PAGE_SIZE with verdict filter chips."""
    template = env.get_template("section.html")
    base_url = SITE.base_url
    for key, section in SECTIONS.items():
        items = sections.get(key, [])
        pages = paginate(items, PAGE_SIZE)
        total_pages = len(pages)
        breadcrumb = [("Home", "/"), (section.title, section.path)]
        for idx, page_items in enumerate(pages, start=1):
            url_path = _feed_path(section.path, idx)
            html = template.render(
                nav_active=key,
                canonical_path=url_path,
                page_title=f"{section.title} — Fact Checks · {SITE.short_name}",
                meta_description=(
                    f"{section.blurb} {len(items)} claims fact-checked so far."
                ),
                og_type="website",
                og_image_path=_DEFAULT_OG_PATH,
                jsonld_blocks=[
                    seo.item_list(page_items, base_url=base_url),
                    seo.breadcrumbs(breadcrumb, base_url=base_url),
                ],
                section=section,
                breadcrumb=breadcrumb,
                checks=page_items,
                page=idx,
                total_pages=total_pages,
                base_path=section.path,
            )
            _write(out_dir, url_path, html)


def _render_articles(
    env: Environment,
    out_dir: Path,
    checks: list[Check],
    by_id: dict[str, Check],
    topic_slugs: set[str],
) -> None:
    """One article per check — the core unit of the site."""
    template = env.get_template("article.html")
    base_url = SITE.base_url
    for check in checks:
        breadcrumb = [
            ("Home", "/"),
            (check.section.title, check.section.path),
            (_truncate(check.claim, 60), check.path),
        ]
        html = template.render(
            nav_active=check.section.key,
            canonical_path=check.path,
            # "Fact Check" = the query modifier; verdict = the answer in the
            # SERP; no brand suffix (Google shows the site name separately).
            page_title=(
                f"Fact Check: {_truncate(check.claim, 50)} — {check.verdict.key}"
            ),
            meta_description=_meta_description(check),
            og_type="article",
            og_image_path=check.og_path,
            jsonld_blocks=[
                seo.claim_review(check, base_url=base_url),
                seo.news_article(check, base_url=base_url),
                seo.breadcrumbs(breadcrumb, base_url=base_url),
            ],
            check=check,
            breadcrumb=breadcrumb[:2],  # visible crumb stops at the section
            related=_resolve_related(check, by_id, checks),
            # Entities with topic pages link there ("Filed Under" row); the
            # rest render unlinked — no dead links, and the row doubles as
            # the internal-link path that keeps topic hubs from being orphans.
            topic_slugs=topic_slugs,
        )
        _write(out_dir, check.path, html)


def _render_topics(
    env: Environment, out_dir: Path, entity_groups: list[EntityGroup]
) -> None:
    """Entity hubs (only entities with >= ENTITY_MIN_CLAIMS earn a page)."""
    template = env.get_template("topic.html")
    base_url = SITE.base_url
    for group in entity_groups:
        entity = group.entity
        members = list(group.checks)
        pages = paginate(members, PAGE_SIZE)
        total_pages = len(pages)
        breadcrumb = [("Home", "/"), (entity.name, entity.path)]
        for idx, page_items in enumerate(pages, start=1):
            url_path = _feed_path(entity.path, idx)
            html = template.render(
                nav_active=None,
                canonical_path=url_path,
                page_title=f"{entity.name} — Fact Checks · {SITE.short_name}",
                meta_description=(
                    f"{len(members)} claims mentioning {entity.name}, "
                    "each fact-checked against independent sources by Lenz."
                ),
                og_type="website",
                og_image_path=_DEFAULT_OG_PATH,
                jsonld_blocks=[
                    seo.item_list(page_items, base_url=base_url),
                    seo.breadcrumbs(breadcrumb, base_url=base_url),
                ],
                entity=entity,
                breadcrumb=breadcrumb,
                claim_count=len(members),
                checks=page_items,
                page=idx,
                total_pages=total_pages,
                base_path=entity.path,
            )
            _write(out_dir, url_path, html)


