def _render_feed_collection(
    env: Environment,
    out_dir: Path,
    *,
    template_name: str,
    base_path: str,
    items: list[Check],
    nav_active: str | None,
    page_title: str,
    meta_description: str,
    extra: dict[str, Any],
) -> None:
    """Shared driver for latest + collection pages (identical page mechanics)."""
    template = env.get_template(template_name)
    base_url = SITE.base_url
    pages = paginate(items, PAGE_SIZE)
    total_pages = len(pages)
    # Breadcrumb label from the heading; strip the leading article ("The …").
    label = extra.get("heading", page_title)
    breadcrumb = [("Home", "/"), (label, base_path)]
    for idx, page_items in enumerate(pages, start=1):
        url_path = _feed_path(base_path, idx)
        html = template.render(
            nav_active=nav_active,
            canonical_path=url_path,
            page_title=page_title,
            meta_description=meta_description,
            og_type="website",
            og_image_path=_DEFAULT_OG_PATH,
            jsonld_blocks=[
                seo.item_list(page_items, base_url=base_url),
                seo.breadcrumbs(breadcrumb, base_url=base_url),
            ],
            breadcrumb=breadcrumb,
            checks=page_items,
            page=idx,
            total_pages=total_pages,
            base_path=base_path,
            **extra,
        )
        _write(out_dir, url_path, html)


